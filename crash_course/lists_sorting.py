locations = ["china", "india", "brazil", "uk", "us"]
print(locations) # prints list in order
print(sorted(locations)) # sorts list alphabeticaly and non permanently
print(locations)  # checks above
#########################################################################
print(sorted(locations, reverse=True)) # reverses list and none permanently
print(locations) # checks list is still in origional order

locations.reverse() # reverses list permanently 
print(locations) # checks above

locations.reverse() # reverses list permanently 
print(locations) # checks above
########################################################################
locations.sort() # sorts list permanently
print(locations) # checks above

locations.reverse()
print(locations) # checks above



